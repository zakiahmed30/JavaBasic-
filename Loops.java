package conditions;

public class Loops {
    public static void main(String[] args) {

        //for loops
        //--loopConditionStarts
        //for (dataType variable = whereToStart;whereToFinish;Increment/Decrement )
        for (int a = 0; a < 5; a++) {
            System.out.println(" Books");
        }
        //while loop

        int x = 3;
        while (x<5) {
            System.out.println("Java Books");
            x++;
        }

    }
}